<template>
  <div class="main">
    <img id="imgRefrencia" src="https://mecaluxco.cdnwm.com/blog/img/almacenamiento-alta-densidad.1.2.jpg">
  <div id="login" >
    <div class="caja">
      <form id= "formulario" class="caja" @submit.prevent="login">
        <h1 class="titulo" id="titulo">Bienvenidos</h1>
        <label class="subtitulo">Correo</label>
        <br>
        <input
          type="email"
          v-model="correo"
          id="inputCorreo"
          required
          :class="inputClass"
          @input="isValidEmail()"
        />
        <p v-if="!validcorreo" id="alertUser">Correo invalido</p>
        <br>
        <label class="subtitulo">Contrase単a</label>
        <div id="passDiv" class="">
          <input
            required
            id="inputPass"
            :type="passwordFieldType"
            v-model="password"
            :class="inputClass"
            @input="isValidPwd()"
          />

          <button
            type="button"
            @click="togglePasswordVisibility"
            id="btnView"
          >
            <img src="../assets/view.svg" id="eyeboton" />
          </button>
        </div>
        <p v-if="!validPwd" id="alertpwd">{{ invalidPwd }}</p>

        <h2 v-if="messageServer">{{ messageServer }}</h2>
        <button type="submit" id="btnLogin" class="btn btn-white border border-black m-2">
          Iniciar Sesion
        </button>
      </form>
    </div>
  </div>
</div>
  <Loader v-if="this.loading"></Loader>
</template>
<script>
import Loader from "./Loader.vue";
import { ref } from "vue";
import api from "@/services/apiUsers.js"
import './styles/login.css'
export default {
  components: {
    Loader,
  },
  data() {
    return {
      correo: null,
      password: null,
      passwordFieldType: "password",
      messageServer: null,
      validcorreo: ref(true),
      validPwd: ref(true),
      url: import.meta.env.VITE_BASE_URL,
      loading: ref(false),
    };
  },
  mounted() {
    this.validateToken();
  },
  methods: {
    returnLogin() {
      this.$router.push("/register");
    },
    async login() {
    //  this.loading = true;
      const data = {
        user: {
          email: this.correo,
          pwd: this.password,
        },
      };
      const datos =await api._login(data);
      if (datos.response.error) {
                  console.log(datos.response.error);
                  this.messageServer = datos.response.error;
                  this.loading = false;
                  return;
                }
      await this.$store.dispatch("login", datos.response.userData);
                localStorage.setItem("jwtToken", datos.response.token);
                this.loading = false;
                this.$router.push("/dashboard");
      
    },
    togglePasswordVisibility() {
      this.passwordFieldType =
        this.passwordFieldType === "password" ? "text" : "password";
    },
    validateToken() {
      const jwt = localStorage.getItem("jwtToken");
      if (jwt) {
        fetch(this.url + "/users/validate", {
          method: "post",
          headers: {
            "content-type": "application/json",
            Authorization: jwt,
          },
        })
          .then((response) => {
            if (response.status == 200) {
              response.json().then(async (data) => {
                await this.$store.dispatch("login", data.response.userData);
                this.$router.push("/dashboard");
              });
            }
            if (response.status == 401) {
              return;
            }
          })
          .catch(() => {
            console.log("error");
          });
      }
    },
    isValidPwd() {
      this.invalidPwd = "";
      this.validPwd = true;
      if (this.password == null) {
        this.invalidPwd = "Campo no puede ser nulo";
        this.validPwd = false;
      }
      if (this.password.length < 4) {
        this.invalidPwd = this.invalidPwd + " Contrase単a menor a 4";
        this.validPwd = false;
      }
      if (this.password.includes(" ")) {
        this.invalidPwd = this.invalidPwd + " Contrase単a contiene vacios";
        this.validPwd = false;
      }
      if (this.password.length > 16) {
        this.invalidPwd = this.invalidPwd + " Contrase単a mayor a 16";
        this.validPwd = false;
      }
    },
    isValidEmail() {
      this.validcorreo = true;
      this.validcorreo = /^[^@]+@\w+(\.\w+)+\w$/.test(this.correo);
      return this.validcorreo;
    },
  },
};
</script>
